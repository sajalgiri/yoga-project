 <h1 class="one"><a name="one">1.Muladhara (Root Chakra)</a></h1>
<h3>Element: Earth</h3>
<h3>Color: Red</h3>
<h3>Sound: Lam</h3>
<p class="one">The Muladhara governs your family ties and feelings of survival, belonging, and guardedness. 
Your earliest memories are stored here, including whether or not your basic needs were met. When it is blocked or 
out of balance, you can become needy, have low self-esteem, or have self-destructive behaviors. 
When Muladhara is in balance, you feel strong and confident; you can stand up on your own two feet and take care of yourself.</p>
