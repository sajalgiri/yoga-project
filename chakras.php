<p class="one">Start by thinking of chakras as a blueprint for your own self-care, and your yoga practice as the architect that makes that blueprint a reality.
 The most direct way to use the chakras is to learn how each one is associated with an element in nature. 
 As Alan Finger, founder of ISHTA Yoga, explains, the first five chakras are associated with the physical elements earth, water, fire, air, and ether (or space). 
 The last two chakras are thought to connect us beyond the earthly realm, so they are associated with the elements of light and cosmic energy.</p>
 
<p class="one">Once you learn the element that each chakra is associated with, you can start to suss out how that element feels in your body. 
 And thinking about your body in these symbolic terms can help you access new stores of energy with the practices detailed in these pages. 
 For example, the root chakra is associated with earth. When it’s in balance, we feel strong and grounded; when it’s out of balance, we may feel unrooted and insecure. 
 Or take the pelvic chakra, which is associated with water. When it’s in balance, we feel fluid and like our creative juices are flowing. 
 When it’s not, we might feel rigid, dry, or emotionally brittle, like a plant that hasn’t been watered enough.</p>
 
<p class="one"> In order to restore balance in your chakras, you must first tune in to how you’re feeling, then figure out which chakra to stimulate to counteract the imbalance. 
 For example, if you’re feeling low in energy, you can do poses that target the navel chakra to rekindle your inner fire. 
 If you’re feeling anxious and long to feel more grounded, choose poses for the earthy root chakra. 
 Or if you seek more courage to speak your truth, the right poses can open and stimulate the throat chakra.</p>
 
 <p class="one">The effects of a chakra-based practice can have a tangible, empowering ripple effect on your life. 
 Jasmine Tarkeshi, vinyasa teacher and cofounder of Laughing Lotus Yoga Center, says she’s been 
 doing more root-chakra practices since becoming a new mom, and the effect is palpable. 
 “If I’m feeling frenzied, I hold the poses longer to feel more grounded and present,” she says. 
 “It informs the rest of my day to the point where maybe I’m not losing my keys so much or I’m not so busy or forgetful that I skip lunch.
 If I specifically use the poses medicinally rather than just haphazardly, I can really change my day.” </p>
 
