 <h1 style="color:blue">Vrksasana (Tree Pose)</h1>
<p class="one">Stand with your feet hip-width apart, creating a stable base. 
On an exhale, soften your knees, and release your tailbone as you engage your thighs. 
Draw the sole of your right foot to the inside of your left inner thigh or calf; continue 
dropping your tailbone and engaging the standing leg’s thigh to keep the stable alignment you had standing on both feet. 
Press through your left foot as you lift through the crown of your head. Hold for 5 breaths, and switch sides.
 Allow gravity to root you down, while noticing how prana moves up your spine.</p>
 
 
 <h1 class="one"><a name="two">2.Svadhisthana (Sacral or Pelvic Chakra)</a></h1>
<h3>Element: Water</h3>
<h3>Color: Orange</h3>
<h3>Sound: Yam</h3>
 <p class="one">This chakra corresponds with your reproductive and sexual organs, and represents fluidity, creativity, and fertility. 
You can take a literal interpretation of this, or associate this chakra with whether or not you feel deserving of a pleasurable, abundant, creative life.
 When it’s out of balance, you can feel emotionally unstable, guilty, or hard on yourself.
 When Svadhisthana in balance, you feel creative, positive, and receptive to change—like the ocean and its tides, you’re in the flow.</p>
 <img src="c2.jpg" width="25%"></img>
 <h1 style="color:blue"> Deviasana (Goddess Pose)</h1>
 <p class="one">Step your feet wide, turn your toes out, and sink your hips far enough to bring each knee over its corresponding ankle.
 Place your hands on your thighs and draw your tailbone down as the pubis lifts. 
 Breathe deeply and move side to side, rocking your pelvis back and forth. You can fold down and move your arms side to side between your feet. 
 The point is to enjoy the movement. Feel free to sigh or make sounds. Hold for 8-10 breaths.
 By opening the hips, you draw focus to the reproductive organs; in swaying, you recognize life’s ebb and flow.</p>
 
 <h1 class="one"><a name="three">3.Manipura (Navel Chakra)</a></h1>
 <h3>Element: Fire</h3>
 <h3>Color: Yellow</h3>
<h3>Sound: Ram</h3>
  <p class="one">You’ve heard the expression “firing on all cylinders.” When the Manipura is in balance, you feel 
  alive and have the self-esteem and confidence to take action and be productive. When it’s blocked, you lack courage, have low self-esteem, 
 and feel stagnant and inert. By working on this chakra, you can awaken your true personal inner power and work through your fear of taking risks.</p>
  <img src="c3.jpg" width="25%"></img>
 <h1 style="color:blue"> Navasana (Boat Pose)</h1>
<p class="one"> Begin seated with your legs ahead of you. Hug your knees into your chest, and then grab behind your knees 
 to help lift your feet off the floor and balance on your sitting bones. Lift your chest, and draw your shoulders down. 
 Shift your weight forward to the front of your sitting bones as you draw in your navel, engaging your abdominals,
 and extend your arms forward and your legs up into Navasana. As you exhale, cross your arms at your chest, 
 and lower your legs until they’re a few inches off the ground; 
 inhale to rise back to Navasana. Repeat 5 times, and then lower to your back. 
 Boat is an energizing pose that ignites your core muscles, creating power for transformation.</p>
 
 <h1 class="one"><a name="four">4.Anahata (Heart Chakra)</a></h1>
 <h3>Element: Air</h3>
<h3>Color: Green</h3>
<h3>Sound: Yam</h3>
<p class="one">Awaken to the power of unconditional love within you through compassion, forgiveness, and acceptance.
 When the heart chakra is blocked, you become possessive and codependent, and may form dysfunctional relationships. You may also stay isolated for fear of rejection.
 When you stimulate the Anahata chakra, you can heal past wounds by reopening your heart, learn to love unconditionally, and form healthy relationships.</p>
 <img src="c4.jpg" width="25%"></img>
 <h1 style="color:blue"> Ustrasana (Camel Pose)</h1>
 <p class="one">Come to your knees, and sit back on your heels. Join your hands at your heart center. Tuck your toes and rise 
 to bring your hips over your knees, making sure knees and toes are hip-width apart. Place your palms on your lower back with the fingers 
 pointing up and gently draw your sacrum down, as your front hip bones lift. Keep your chin in toward your chest, and lean back.
 Hug the shoulder blades toward each other. Stay here and breathe, or reach for your heels with your hands. The head is the last thing to
 release, if it’s comfortable. After a few breaths, bring your hands back to your sacrum and sit on your heels,
 returning your hands to prayer and bowing your head. Camel opens the heart center. Before you arch back, consider 
 dedicating the posture to someone for whom you feel compassion.</p>
  
  <h1 class="one"><a name="five">5.Vishuddha (Throat Chakra)</a></h1>
 <h3>Element: Ether</h3>
<h3>Color: Blue</h3>
<h3>Sound: Ham</h3>
<p class="one">When Vishuddha is blocked, you may feel like you can’t find your voice or your truth. You may also be overly talkative and not listen to others. 
 When this chakra is open and stimulated, your voice moves through space to help you communicate your emotions in healthy ways. You also become
 better at listening to others and honoring their personal truths without judgment.</p>
 <img src="c5.jpg" width="25%"></img>
 <h1 style="color:blue"> Salamba Sarvangasana (Supported Shoulderstand)</h1>
 <p class="one"> Lie down with your shoulders supported on a folded blanket, head on the floor. Bend your knees, rock your hips up, lift your legs
 overhead, and then release your toes toward the floor beyond the crown of your head. Place your hands midback, and lift one leg at 
 a time skyward. Allow your gaze to drop toward your heart, and hear the sound of your breath. Feel free to express yourself by joining the
 soles of the feet, or by lowering one leg at a time toward the floor. Hold for up to 2 minutes. To release, lower both feet to the floor
 above your head, release your hands to the floor, and lower yourself vertebra by vertebra. Freeing the neck and spine, and 
 then turning the senses in toward your breath, allows you to connect with your own rhythm.</p>
 
 
  <h1 class="one"><a name="six">6.Anja (Third-Eye Chakra)</a><h1>
 <h3>Element: Light</h3>
<h3>Color: Indigo</h3>
<h3>Sound: OM</h3>
 <p class="one">This chakra is associated with your intuition, or sixth sense, and governs how the rest of the chakras function. 
 When Ajna is functioning well, you have insight, and you trust your inner wisdom to face life’s challenges and choices. When it’s blocked,
 you feel close-minded, too attached to logic, untrusting, and cynical. Working on the sixth chakra opens your mind to the bigger picture 
 and different perspectives, and it helps you receive the wisdom that cannot be seen or heard by ordinary senses.</p>
  <img src="c6.jpg" width="25%"></img>
 <h1 style="color:blue"> Sukhasana (Easy Pose)</h1>
  <p class="one">Come to a seat. Fold one heel in toward your groin, and then the other. If your knees aren’t lower than your hips, 
  sit on a folded blanket. Cup your palms toward each other, touching opposite fingertips in Hakini Mudra. For 10 breaths, close your eyes,
  pose a question to yourself, and focus on the sound of your breath, placing the tip of your tongue to the roof of your mouth as you inhale,
  and relaxing it as you exhale. Release the backs of your hands to your knees, and see if you sense an answer. Stay here for up to 5 minutes. 
  Hakini Mudra is known to increase the power of concentration, and in this pose you can easily access calm focus.</p>
 
  <h1 class="one"><a name="seven">7.Sahasrara (Crown Chakra)</a><h1>
 <h3>Element: Cosmic Energy</h3>
<h3>Color: Violet or White</h3>
<h3>Sound: OM</h3>
<p class="one"> The crown chakra connects to beauty itself and the spiritual realm. It helps you to understand who you are beyond your physical
 self—that you are a spiritual being having a human experience. It is not located in the body but actually hovers above the crown of the head.
 When it’s closed, you think happiness can only come from the outside, and you suffer. Working on this chakra helps you to feel free in any situation.</p>
  <img src="c7.jpg" width="25%"></img>
 <h1 style="color:blue"> Savasana (Corpse Pose)</h1>
 <p class="one"> Make sure you’re warm and comfortable, and lie down on your back. You can cover yourself with a blanket, cover your eyes with an 
 eye pillow, or place a rolled-up blanket under your knees or head. Open your legs hip-width apart, and release your arms to your sides with your 
 palms facing up. Take a deep breath and squeeze every part of your body tight, lifting your head, arms, and legs off the floor. Hold for a moment,
 and let everything go with a big exhale out of the mouth. Do this several times. Imagine a lotus flower at the crown of your head. With every inhale, 
 imagine Divine light pouring in through the flower, and with every exhale, let go of anything that binds you to the past. Stay for 5–20 minutes, then
 slowly bring your awareness back to your breath, and move your fingers and toes to reconnect to your physical body without losing your connection to your infinite self.</p>
 
 
